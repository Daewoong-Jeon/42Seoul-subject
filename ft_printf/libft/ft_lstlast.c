#include "libft.h"

t_info		*ft_lstlast(t_info *lst)
{
	t_info	*tmp;

	tmp = lst;
	if (tmp == NULL)
		return (0);
	while (tmp->next != '\0')
		tmp = tmp->next;
	return (tmp);
}
