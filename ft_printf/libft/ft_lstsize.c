#include "libft.h"

int		ft_lstsize(t_info *lst)
{
	t_info	*tmp;
	int		count;

	tmp = lst;
	count = 0;
	while (tmp != '\0')
	{
		count++;
		tmp = tmp->next;
	}
	return (count);
}
